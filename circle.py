import math


def area(r):
    '''
    Вычисляет площадь круга
    Для вычисоения используется стандартная формула:
    S = pi * r^2, где pi - число пи, r - радиус окружности
    Параметры:
        r (float): радиус круга
    Возвращает:
        float: площадь круга
    Пример использования:
        area(10) -> 314.1592653589793
    '''
    if (r >= 0):
        return math.pi * r * r
    else:
        raise ValueError("Radius must be >= 0")


def perimeter(r):
    '''
    Вычисляет длину окружности
    Для вычисления используется стандартная формула:
    P = 2 * pi * r, где pi - число пи, r - радиус окружности
    Параметры:
        r (float): радиус окружности
    Возвращает:
        float: длина окружности
    Пример использования:
        perimetr(10) -> 62.83185307179586
    '''
    if (r >= 0):
        return 2 * math.pi * r
    else:
        raise ValueError("Radius must be >= 0")
